export class DefaultMapLoader {

  CHUNK_SIZE = 2;

  BLOCK_SIZE = 32;
  BLOCK_SIZE_Z = 2;

  constructor(heigthModifier = 1) {
    this.map = [],
      this.mapInfos = {}
    this.heigthModifier = heigthModifier;
  }

  async initMap() {

    this.cellProperties = prepareCellDefinitions();
    this.placeableProperties = preparePlaceableDefinitions();
    console.log("object Properties", this.cellProperties, this.placeableProperties);
    this.map = [];
    for (let z = 0; z < this.heigthModifier; z++) {
      this.map.push(...DEFAULT_MAP);
    }
    this.mapInfos.size = { x: 32, y: 32, z: 2 * this.heigthModifier };
    this.placeables = [];
    for (let z = 0; z < this.heigthModifier; z++) {
      this.placeables.push(...DEFAULT_PLACEABLES);
    }

    return {
      x: 16,
      y: 0,
      z: 0
    }
  }

  async loadChunk(x, y, z, size, forceReload = true) {
    console.log("loading chunk but for test map so do nothing : ", { x, y, z, size, forceReload });
  }

}

export class DfMapLoader {

  CHUNK_SIZE = 2;

  BLOCK_SIZE = 16;
  BLOCK_SIZE_Z = 1;

  constructor() {
    this.map = [],
    this.mapInfos = {}
    this.client = new DwarfClient();
  }

  ready() {
    return this.client ? this.client._initialized : false
  }

  async initMap() {

    this.cellProperties = prepareCellDefinitions();
    this.placeableProperties = preparePlaceableDefinitions();
    console.log("blockProperties", this.cellProperties);

    await this.ready();

    const dfMapInfos = await this.client.GetMapInfo();
    console.log(dfMapInfos);
    this.mapInfos.size = {
      x: dfMapInfos.blockSizeX * this.BLOCK_SIZE,
      y: dfMapInfos.blockSizeY * this.BLOCK_SIZE,
      z: dfMapInfos.blockSizeZ * this.BLOCK_SIZE_Z
    };

    this.map = new Array(this.mapInfos.size.z).fill(0).map(() => new Uint8Array(this.mapInfos.size.x * this.mapInfos.size.y));
    this.placeables = new Array(this.mapInfos.size.z).fill(0).map(()=>[]);

    const tileTypeList = await this.client.GetTiletypeList();

    this.tileTypeList = tileTypeList.tiletypeList;
    console.log(tileTypeList);

    return {
      x: 48,
      y: 123,
      z: 152
    }
  }

  async loadChunk(x, y, z, size, forceReload = true) {
    await this.ready();
    const params = { minX: x, minY: y, minZ: z, maxX: x + size + 1, maxY: y + size + 1, maxZ: z + size + 1, forceReload };
    console.log("loading chunk : ", params);
    try {
      let res = await this.client.GetBlockList(params);
      this._processDfBlocks(res.mapBlocks || []);
    } catch (e) {
      console.log(e);
    }
  }

  _processDfBlocks(blocks) {
    for (let block of blocks) {
      let basePosition = {
        x: block.mapX,
        y: block.mapY,
        z: block.mapZ
      }
      for (let x = 0; x < 16; x++) {
        for (let y = 0; y < 16; y++) {
          let index = y * 16 + x;
          let tileType = this.tileTypeList.find(t => t.id === block.tiles[index]);
          if (!tileType) {
            console.log("Tile type not found for", block.tiles[index]);
            continue;
          }
          this.map[basePosition.z][((basePosition.y + y) * this.mapInfos.size.x) + basePosition.x + x] = this._mapDFInfosToBlock(tileType.shape, tileType.material, tileType.special, this.cellProperties.correspondances);
        }
      }
      for (let building of block.buildings || []) {
        if (building.buildingType && this.placeableProperties.correspondances[building.buildingType.buildingType]) {
          this.placeables[building.posZMin].push({
            x: building.posXMin + 0.5,
            y: building.posYMin + 0.5,
            type: this.placeableProperties.correspondances[building.buildingType.buildingType]
          });
        }
      }
    }
    console.log("Block groub loaded", blocks);
  }

  _mapDFInfosToBlock(shape, material, special) {
    if (shape == -1 || shape == 0) {
      return 0;
    }
    let key = `${shape},${material},${special}`;

    if (this.cellProperties.correspondances[key] !== undefined) {
      return this.cellProperties.correspondances[key];
    }

    key = `${shape},${material},-1`;

    if (this.cellProperties.correspondances[key] !== undefined) {
      return this.cellProperties.correspondances[key];
    }

    key = `${shape},-1,-1`;

    if (this.cellProperties.correspondances[key] !== undefined) {
      return this.cellProperties.correspondances[key];
    }

    console.log("no last correspondance for", key, shape, material, special);

    return 1;
  }
}

